const POOLS: Record<ReviewKind, string[]> = { seed: SEED_TEXTS, szr: SZR_TEXTS, fert: FERT_TEXTS, tech: TECH_TEXTS, soft: SOFT_TEXTS }
const METRIC: Record<ReviewKind, (r: () => number) => string> = {
  seed: (r) => `получено ${25 + Math.floor(r() * 18)} ц/га`,
  szr: (r) => `прибавка +${3 + Math.floor(r() * 6)} ц/га`,
  fert: (r) => `прибавка +${2 + Math.floor(r() * 5)} ц/га`,
  tech: (r) => `наработка ${3 + Math.floor(r() * 12)} тыс. га`,
  soft: (r) => `экономия ${300 + Math.floor(r() * 900)} ₽/га`,
}

const pad = (n: number) => String(n).padStart(2, '0')

export function reviewsFor(id: string, kind: ReviewKind): Review[] {
  const rnd = mulberry32(hash(id + kind))
  const count = 3 + Math.floor(rnd() * 5) // 3–7
  const texts = POOLS[kind]
  const out: Review[] = []
  for (let i = 0; i < count; i++) {
    const r = rnd()
    const rating = r < 0.5 ? 5 : r < 0.82 ? 4 : r < 0.95 ? 3 : 2
    const metric = METRIC[kind](rnd)
    out.push({
      id: id + '-rv' + i,
      farmer: FARMERS[Math.floor(rnd() * FARMERS.length)],
      farm: FARMS[Math.floor(rnd() * FARMS.length)],
      region: REGS[Math.floor(rnd() * REGS.length)],
      crop: CROPS[Math.floor(rnd() * CROPS.length)],
      rating,
      date: `${pad(1 + Math.floor(rnd() * 28))}.${pad(6 + Math.floor(rnd() * 4))}.${24 + Math.floor(rnd() * 2)}`,
      text: texts[Math.floor(rnd() * texts.length)],
      verified: rnd() > 0.2,
      metric,
      helpful: Math.floor(rnd() * 24),
    })
  }
  return out
}

export function ratingFor(id: string, kind: ReviewKind) {
  const rs = reviewsFor(id, kind)
  const avg = rs.reduce((s, r) => s + r.rating, 0) / rs.length
  const dist = [5, 4, 3, 2, 1].map((star) => ({ star, n: rs.filter((r) => r.rating === star).length }))
  return { avg: Math.round(avg * 10) / 10, count: rs.length, dist }
}
